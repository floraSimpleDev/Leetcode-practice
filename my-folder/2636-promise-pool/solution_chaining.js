/**
 * @param {Function[]} functions
 * @param {number} n
 * @return {Promise<any>}
 */
var promisePool = async function(functions, n) {
    const executeNext = () => functions[n++]?.().then(executeNext);

    return await Promise.all(functions.slice(0, n).map(fn => fn().then(executeNext)));
};

